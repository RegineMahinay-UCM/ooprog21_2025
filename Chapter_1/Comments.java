

/**
 * This is the Comments class.
 * It demonstrates the three types of comments in Java:
 * 1. Single-line comments
 * 2. Multi-line comments
 * 3. Documentation comments
 */
public class Comments {

    public static void main(String[] args) {

        // This is a single-line comment.
        // It explains the line of code below:
        System.out.println("This line is explained by a single-line comment.");

        /*
         * This is a multi-line comment.
         * You can write across multiple lines.
         * It's often used to explain more complex logic or sections of code.
         */
        System.out.println("This line is explained by a multi-line comment.");
    }
}
